const mongo = require('mongoose')

const Schema = mongo.Schema


const UserSchema = new Schema({
    userName: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        default: "user"
    }
})

const User = mongo.model("Users", UserSchema)
module.exports = User